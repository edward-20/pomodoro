'use strict';

chrome.alarms.onAlarm.addListener(() => {
  chrome.action.setBadgeText({ text: '' });
  chrome.notifications.create({
    type: 'basic',
    iconUrl: 'stay_hydrated.png',
    title: 'Take a Break!',
    message: "You've finished your study/work period, take a well deserved break.",
    priority: 0
  });
});